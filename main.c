#include "cmatrix.h"

int main()
{
	matrix* d;
	d = Mnew(2, 2);
	d->A[0][0] = 1;
	d->A[0][1] = 2;
	d->A[1][0] = 3;
	d->A[1][1] = 4;
	
	printf("输入的矩阵为：\n");
	Mprintf(d);
	matrix* dinv, * dt, * ddt, * ddinv, * dprd, * dmd, * ddd, * dpld;
	dinv = Minv(d);
	printf("矩阵逆为：\n");
	Mprintf(dinv);
	dt = Mtrans(d);
	printf("矩阵转置为：\n");
	Mprintf(dt);
	ddt = Mmulti(d, dt);
	printf("矩阵与转置相乘：\n");
	Mprintf(ddt);
	ddinv = Mmulti(d, dinv);
	printf("矩阵与逆相乘为：\n");
	Mprintf(ddinv);
	dprd = Mdotpro(d, d);
	printf("矩阵点乘为：\n");
	Mprintf(dprd);
	ddd = Mdiv(d, d);
	printf("矩阵点除为：\n");
	Mprintf(ddd);
	dmd = Mminus(d, d);
	printf("矩阵相减为：\n");
	Mprintf(dmd);
	dpld = Mplus(d, d);
	printf("矩阵相加为：\n");
	Mprintf(dpld);

	Mfree(d);
	Mfree(dt);
	Mfree(dinv);
	Mfree(ddt);
	Mfree(ddinv);
	Mfree(dprd);
	Mfree(ddd);
	Mfree(dmd);
	Mfree(dpld);

	return 0;
}